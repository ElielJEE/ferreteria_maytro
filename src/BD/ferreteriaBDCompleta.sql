--
-- TABLA: USUARIOS_CREDITOS
--
CREATE TABLE USUARIOS_CREDITOS (
    ID_USUARIOSCRED    VARCHAR(10)    NOT NULL,
    MONTO_PAGO         VARCHAR(20),
    MONTO_DEUDA        DECIMAL(12,2)         NOT NULL,

DROP TABLE IF EXISTS DETALLES_COMPRA;
DROP TABLE IF EXISTS COMPRAS;
DROP TABLE IF EXISTS APERTURA_CAJA;
DROP TABLE IF EXISTS PRODUCTOS;
DROP TABLE IF EXISTS NIVELACION;
